export const getMockCars = (filters: {
  manufacturer?: string;
  year?: number;
  model?: string;
  fuel?: string;
  limit?: number;
}) => {
  let filteredCars = [...mockCars];

  // Filter by manufacturer
  if (filters.manufacturer && filters.manufacturer.trim() !== '') {
    filteredCars = filteredCars.filter(car => 
      car.make.toLowerCase().includes(filters.manufacturer!.toLowerCase())
    );
  }

  // Filter by model
  if (filters.model && filters.model.trim() !== '') {
    filteredCars = filteredCars.filter(car => 
      car.model.toLowerCase().includes(filters.model!.toLowerCase())
    );
  }

  // Filter by year
  if (filters.year) {
    filteredCars = filteredCars.filter(car => car.year === filters.year);
  }

  // Filter by fuel type
  if (filters.fuel && filters.fuel.trim() !== '') {
    filteredCars = filteredCars.filter(car => 
      car.fuel_type.toLowerCase().includes(filters.fuel!.toLowerCase())
    );
  }

  // Apply limit
  const limit = filters.limit || 10;
  return filteredCars.slice(0, limit);
};
